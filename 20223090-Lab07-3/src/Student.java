public class Student {
	public String names;
	public int scores;
	
	public Student(String names, int scores) {
		this.names = names;
		this.scores = scores;
	}
	public String getNames() {
		return names;
	}
	public int getScores() {
		return scores;
	}
	public void setNames(String names) {
		this.names=names;
	}
	public void setScores(int scores) {
		this.scores=scores;
	}
}
